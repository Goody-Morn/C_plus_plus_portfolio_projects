#include <iostream>
using namespace std;

int main()
{
	cout << "\tEscape Sequences\n\n";
	cout << "\\a\tBell (alert)\n";
	cout << "\\b\tBackspace\n";
	cout << "\\n\tNew line\n";
	cout << "\\r\tCarriage return\n";
	cout << "\\t\tHorizontal tab\n";
	cout << "\\\\\tBackslash \\\n";
	cout << "\\t\tHorizontal tab\n";
	cout << "\\\"\tDouble quotation mark \"\n";
	cout << "\\\'\tSingle quotation mark '\n";
	return 0;
}
